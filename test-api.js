const http = require('http');

const options = {
  hostname: 'localhost',
  port: 5000,
  path: '/api/exam-types',
  method: 'GET',
  headers: {
    'Content-Type': 'application/json'
  }
};

console.log('Testing API: GET /api/exam-types');

const req = http.request(options, (res) => {
  let data = '';
  
  res.on('data', (chunk) => {
    data += chunk;
  });
  
  res.on('end', () => {
    console.log('Status Code:', res.statusCode);
    console.log('Response:', data);
  });
});

req.on('error', (error) => {
  console.error('Error:', error.message);
});

req.end();

setTimeout(() => {
  process.exit(0);
}, 5000);
